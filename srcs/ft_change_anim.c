#include "wolf3d.h"

void	ft_change_anim(t_mlx *e)
{
	if (e->counter_chu % ANIM_SPEED == 0)
	{
		(e->tab[28][33])++;
		if (e->tab[28][33] == 10)
			e->tab[28][33] = 5;
	}
	(e->counter_chu)++;
}
