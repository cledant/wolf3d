#include "wolf3d.h"

int		ft_check_wall(t_mlx *e, int r_x, int r_y)
{
	if (r_x / 64 >= e->x_max || r_x < 0)
		return (1);
	if (r_y / 64 >= e->y_max || r_y < 0)
		return (1);
	if (e->tab[r_y / 64][r_x / 64] >= 1 && e->tab[r_y / 64][r_x / 64] <= 9)
		return (1);
	else
		return (-1);
}
